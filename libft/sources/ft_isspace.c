#include "libft.h"

/* ft_isspace -- white-space charaacter test */

int	ft_isspace(int c)
{
	if (c == '\t' || c == '\n' || c == '\v' \
			|| c == '\f' || c == ' ' || c == '\r')
		return (1);
	return (0);
}
